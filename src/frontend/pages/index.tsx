import type { NextPage } from 'next';
import Head from 'next/head';
import MenuBar from '../components/MenuBar/notebookOptionsBar';
import NavBar from '../components/NavBar';
import SideBar from '../components/SideBar';
import NoteBookCell from '../components/Cell';
import { useSelector } from "react-redux";
import { AppState, NbCell } from '../lib/typings/types';
import lightTheme from '../styles/themes/lightTheme';
import darkTheme from '../styles/themes/darkTheme';
import { ThemeProvider, CssBaseline } from '@mui/material';

const Home: NextPage = () => {
  const { cellIds, cells, config } = useSelector((state: { app: AppState }) => state.app)

  return (
    <ThemeProvider theme={config.notebookThemeMode == "light" ? lightTheme : darkTheme}>
      <CssBaseline />
      <div>
        <Head>
          <title>Dnotebook - Exciting Notebook experience</title>
          <meta name="description" content="Generated by create next app" />
          <link rel="icon" href="/favicon.ico" />
        </Head>

        <div className='flex-row'>
          <section>
            <NavBar />
          </section>
          <section>
            <MenuBar />
          </section>

          <section style={{ height: "650px" }} className='grid grid-cols-12 p-2 overflow-y-scroll'>
            <div className='fixed'>
              <SideBar />
            </div>
            <div className='col-span-12 border-l-2 ml-60'>
              {
                cellIds.map((cellId: string, i: number) => {
                  const cell: NbCell = cells[cellId]
                  return <div key={cellId}><NoteBookCell cell={cell} /></div>
                })
              }
            </div>

          </section>
        </div>
      </div>
    </ThemeProvider>
  )
}

export default Home
